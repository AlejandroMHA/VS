CREATE  PROCEDURE [dbo].[cnf_sp_Delete_DataSource] (

@CD_DATASOURCE			int out,

@Mensaje				varchar(500) OUT

) 

as


SET @Mensaje = ''
SELECT '1'

DELETE FROM TAP8_WFM_DATASOURCE_QUERY 
WHERE 	
CD_DATASOURCE = @CD_DATASOURCE

DELETE FROM TAP8_WFM_DATASOURCE 
WHERE 	
CD_DATASOURCE = @CD_DATASOURCE
	
DELETE FROM TAP8_WFM_QUERY 
WHERE 	
CD_QUERY = @CD_DATASOURCE



